//2. GET a specific joke by id

app.get('/jokes/:id', (req, res) => {
  const id = parseInt(req.params.id);
  const foundJoke = jokes.find((joke) => joke.id === id); // kode ini akan mencari joke yang memiliki id yang sama dengan id yang diinputkan
  // cara kerja kode di atas adalah dengan menggunakan method find() yang akan mencari joke yang memiliki id yang sama dengan id yang diinputkan
  if (foundJoke) {
    res.json(foundJoke);
  } else {
    res.status(404).json({ message: 'Joke not found' });
  }
});



//3. GET a jokes by filtering on the joke type

app.get('/filter', (req, res) => {
  const jokeType = req.query.jokeType;
  const filteredJokes = jokes.filter((joke) => joke.jokeType === jokeType);
  if(filteredJokes.length > 0) {
    res.json(filteredJokes);
  }
  else {
    res.status(404).json({ message: 'Joke type not found' });
  }
});

